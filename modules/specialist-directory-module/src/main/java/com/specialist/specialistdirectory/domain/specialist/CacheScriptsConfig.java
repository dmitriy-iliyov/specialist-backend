package com.specialist.specialistdirectory.domain.specialist;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.io.ClassPathResource;
import org.springframework.data.redis.core.script.DefaultRedisScript;
import org.springframework.scripting.support.ResourceScriptSource;

@Configuration
public class CacheScriptsConfig {

    @Bean
    public DefaultRedisScript<Boolean> evictAfterSpecialistSaveScript() {
        DefaultRedisScript<Boolean> script = new DefaultRedisScript<>();
        script.setScriptSource(new ResourceScriptSource(new ClassPathResource("cache_evict_after_specialist_save.lua")));
        script.setResultType(Boolean.class);
        return script;
    }

    @Bean
    public DefaultRedisScript<Boolean> evictAfterSpecialistDelScript() {
        DefaultRedisScript<Boolean> script = new DefaultRedisScript<>();
        script.setScriptSource(new ResourceScriptSource(new ClassPathResource("cache_evict_after_specialist_del.lua")));
        script.setResultType(Boolean.class);
        return script;
    }
}
